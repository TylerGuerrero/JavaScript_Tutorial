// CONST
// +------------+
// 1) can not be redeclared or reassigned
// 2) All Scopes (global, function, block)
// 3) a reference to a value
// 4) use for value, array, object, function, regexp
//    can change elements with arrays and objects

// LET
// +-----------+
// 1) can not be redeclared but can be reassigned
// 2) All scopes (global, function, block)
// 3) used for value, array, object, function, regexp


// VAR
// +-----------+
// 1) can be redecalred and reassinged
// 2) has global or funtion scope
// 3) creates property on the window object in global scope
// 4) used for value, array, funtion, object, regexp